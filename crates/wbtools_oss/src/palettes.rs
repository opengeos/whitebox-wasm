    /// Returns the RGB values of a palette in the 0-1 range.
    pub fn reverse(&self) -> Vec<(f32, f32, f32)> {
        let pal = self.get_palette();
        let mut ret = Vec::with_capacity(pal.len());
        for i in (0..pal.len()).rev() {
            ret.push(pal[i]);
        }
        ret
    }

    /// Returns the RGB values of a palette in the 0-1 range.
    pub fn get_normalized_palette(&self) -> Vec<(f32, f32, f32)> {
        let mut pal = self.get_palette();
        for i in 0..pal.len() {
            pal[i].0 /= 256f32;
            pal[i].1 /= 256f32;
            pal[i].2 /= 256f32;
        }
        pal
    }

